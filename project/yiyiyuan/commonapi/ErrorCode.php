    public function getErrorArr($error_code, $error_msg = '') {
        $array['rsp_code'] = (string)$error_code;
        if (!empty($error_msg)) {
            $array['rsp_msg'] = $error_msg;
        } else {
            $array['rsp_msg'] = $this->geterrorcode($error_code);
        }
        return $array;
    }

