<p>第二条 甲方权利与义务</p>
<p>1)甲方在获取乙方贷后服务的全过程中，必须真实准确向乙方提供所要求提供的个人信息，如甲方提供不真实信息或有意隐瞒重要信息，视为违约，须对乙方承担违约责任；</p>
<p>2)甲方自愿授权乙方基于甲方提供的信息及乙方独立获取的信息管理甲方的信用信息，并提供贷后服务；</p>
<p>3)甲方同意，乙方协调甲方按照约定期限及金额进行还款，甲方有义务无条件及时配合乙方工作；</p>
<p>4)乙方为甲方提供仲裁对接服务；</p>
<p>5)甲方有义务按照本协议的规定向乙方支付相关费用。</p>
<p>第三条 乙方权利与义务</p>
<p>1)乙方接受甲方的委托后，须为甲方提供约定的贷后服务的全程信息，并在过程中协助其办理各项手续；</p>
<p>2)乙方有权通过甲方提供的个人信用信息及行为记录进行审核，根据甲方需要可以为甲方提供上述贷后服务内容的相关建议；</p>
<p>3)乙方有权根据乙方对甲方的贷后服务情况决定是否进一步提供贷后服务；</p>
<p>4)若甲方存在违反与出借人签订的协议条款，逾期偿还借款或者存在其他违约行为，乙方有权随时终止为甲方提供贷后服务，且无需另行通知甲方，乙方不会因此而向甲方承担任何违约责任；</p>
<p>5)乙方与甲方达成调解协议，并向仲裁委员会申请仲裁，甲方接受乙方为甲方提供的仲裁对接服务；</p>
<p>6)乙方有权向甲方收取约定的服务费。</p>
