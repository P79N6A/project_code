<p>第一条 服务内容与范围</p>
<p>甲方委托乙方，通过网络、电话、现场等方式向甲方提供贷后服务，包括但不限于：</p>
<p>1)贷后咨询服务</p>
<p>2)贷后跟踪服务</p>
