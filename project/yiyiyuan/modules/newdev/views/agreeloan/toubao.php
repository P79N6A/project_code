    <h3>众安在线财产保险股份有限公司</h3>
    <h3>借款人意外伤害保险条款</h3>
    <h3>(众安在线)(备-普通意外保险)【2017】(主)008号</h3>
    <h3>总则</h3>
    <p>第一条 本保险合同由保险条款、投保单、保险单、保险凭证以及批单等组成。凡涉及本保险合同的约定，均应采用书面形式。</p>
    <p>第二条 本保险合同的被保险人包括：</p>
    <p>（一）十八周岁（含）至六十五周岁（含）、身体健康、能正常工作或正常生活，向自然人、法人组织合法借款的借款人。</p>
    <p>（二）企业或其他社会团体向自然人、法人组织合法借款的，该企业或其他社会团体中年龄在十八周岁（含）至六十五周岁（含）之间（续保者年龄上限可延至六十九周岁（含））身体健康的高级管理人员或控股股东，也可以作为本保险合同的被保险人。</p>
    <p>第三条 本保险合同的投保人应为具有完全民事行为能力的被保险人本人、对被保险人有保险利益的其他人。</p>
    <p>第四条 受益人：</p>
    <p>本保险合同的受益人按照受益顺序分为第一受益人和第二受益人。</p>
    <p>第一受益人为向被保险人合法提供借款的自然人、法人组织，其受益金额为发生保险事故申请理赔时被保险人依本保险合同所附借款合同的约定仍未偿还的借款本息总额，最高以保险金额为限。被保险人提前还清借款本息总额后，合法提供借款的自然人、法人组织不再作为第一受益人，其他受益人的受益顺序依次提前。</p>
    <p>本保险合同身故保险金的第二受益人由被保险人或投保人在订立本保险合同时指定，其受益金额为发生保险事故时保险人给付的保险金扣除第一受益人的受益金额后的剩余部分。被保险人或投保人可指定一人或数人为身故保险金第二受益人。身故保险金第二受益人为数人时，应确定其受益顺序和受益份额；未确定受益份额的，各身故保险金受益人按照相等份额享有受益权。本保险合同的残疾保险金的第二受益人为被保险人本人。</p>
    <p>被保险人或投保人可以变更保险金第二受益人，但需书面通知保险人，由保险人在本保险合同上批注。对因保险金受益人变更发生的法律纠纷，保险人不承担任何责任。</p>
    <p>投保人指定或变更保险金受益人的，应经被保险人书面同意。被保险人为无民事行为能力人或限制民事行为能力人的，应由其监护人指定或变更保险金受益人。</p>
    <p>受益人故意造成被保险人身故、伤残的，或者故意杀害被保险人未遂的，该受益人丧失受益权。</p>
    <h3>保险责任</h3>
    <p>第五条 在保险期间内，被保险人因遭受意外伤害事故导致身故、伤残的，保险人依照下列约定给付保险金。</p>
    <p>（一）身故保险责任</p>
    <p>在保险期间内，被保险人遭受意外伤害事故，并自事故发生之日起180日内因该事故为直接且唯一原因导致其身故的，保险人按本保险合同所载意外伤害保险金金额给付身故保险金，对被保险人的保险责任终止，本保险合同终止。</p>
    <p>被保险人因遭受意外伤害事故且自该事故发生日起下落不明，后经人民法院宣告死亡的，保险人按本保险合同所载意外伤害保险金金额给付身故保险金。但若被保险人被宣告死亡后生还的，保险金受领人应于知道或应当知道被保险人生还后30日内退还保险人给付的身故保险金。</p>
    <p>被保险人身故前保险人已给付本条第（二）款约定的残疾保险金的，所给付的身故保险金应扣除已给付的残疾保险金金额。</p>
    <p>（二）伤残保险责任</p>
    <p>在保险期间内，被保险人遭受意外伤害事故，并自该事故发生之日起180日内因该事故造成本保险合同所附《人身保险伤残评定标准（行业标准）》（简称《伤残评定标准》）所列伤残之一的，保险人按《伤残评定标准》所对应伤残等级的给付比例乘以意外伤害保险金金额给付残疾保险金。如第180日治疗仍未结束的，按当日的身体情况进行伤残鉴定，并据此给付残疾保险金。</p>
    <p>1．当同一保险事故造成两处或两处以上伤残时，应首先对各处伤残程度分别进行评定，如果几处伤残等级不同，以最重的伤残等级作为最终的评定结论；如果两处或两处以上伤残等级相同，伤残等级在原评定基础上最多晋升一级，最高晋升至第一级。同一部位和性质的伤残，不应采用《伤残评定标准》条文两条以上或者同一条文两次以上进行评定。</p>
    <p>2．被保险人如在本次意外伤害事故之前已有伤残，保险人按合并后的伤残程度在《伤残评定标准》中所对应的给付比例给付残疾保险金，但应扣除原有伤残程度在《伤残评定标准》所对应的残疾保险金。</p>
    <p>前述第（一）、（二）款下的保险金累计给付金额以保险合同载明的意外伤害保险金金额为限。在保险期间内，若保险人累计给付的残疾保险金金额达到意外伤害保险金金额时，则保险人对被保险人的保险责任终止，本保险合同终止。</p>

    <h3>责任免除</h3>
    <p>第六条 因下列原因造成被保险人身故或残疾的，保险人不承担给付保险金责任：</p>
    <p>（一）投保人的故意或重大过失行为；</p>
    <p>（二）故意自伤或自杀；</p>
    <p>（三）因被保险人故意犯罪、拒捕、挑衅或故意行为而导致的打斗、被袭击或被谋杀；</p>
    <p>（四）妊娠、流产、分娩、药物过敏；</p>
    <p>（五）接受包括美容、整容、整形手术在内的任何医疗行为而造成的意外；</p>
    <p>（六）被保险人未遵医嘱，私自服用、涂用、注射药物；</p>
    <p>（七）受酒精、毒品、管制药物的影响；</p>
    <p>（八）疾病，包括但不限于高原反应、中暑；</p>
    <p>（九）猝死；</p>
    <p>（十）非因意外伤害导致的细菌或病毒感染；</p>
    <p>（十一）任何生物、化学、原子能武器，原子能或核能装置所造成的爆炸、灼伤、污染或辐射；</p>
    <p>（十二）恐怖袭击。</p>
    <p>第七条 被保险人在下列期间遭受伤害导致身故或残疾的，保险人也不承担给付保险金责任：</p>
    <p>（一）战争、军事行动、武装叛乱或暴乱期间；</p>
    <p>（二）被保险人从事违法、犯罪活动期间或被依法拘留、服刑、在逃期间；</p>
    <p>（三）被保险人存在精神和行为障碍（以世界卫生组织颁布的《疾病和有关健康问题的国际统计分类（ICD-10）》为准）期间；</p>
    <p>（四）被保险人从事高风险运动期间，但被保险人作为该高风险运动的专业运动员除外；</p>
    <p>（五）被保险人酒后驾驶、无有效驾驶证驾驶或驾驶无有效行驶证的机动交通工具期间；</p>
    <p>（六）被保险人驾驶或搭乘非商业航班期间；</p>
    <p>（七）被保险人患艾滋病（AIDS）或感染艾滋病病毒（HIV）期间。</p>
    <h3>保险金额</h3>
    <p>第八条 保险金额是保险人承担给付保险金责任的最高限额。</p>
    <p>本保险合同的保险金额是“意外伤害保险金金额”，由投保人、保险人双方约定，并在保险合同中载明。</p>
    <h3>保险期间</h3>
    <p>第九条 本保险合同保险期间由保险人和投保人协商确定，以保险合同载明的起讫时间为准，最长不超过5年。</p>
    <h3>保险期间</h3>
    <p>保险人</p>
    <p>第十条 本保险合同成立后，保险人应当及时向投保人签发保险单或其他保险凭证。</p>
    <p>第十一条 保险人按照第十八条的约定，认为被保险人提供的有关索赔的证明和资料不完整的，应当及时一次性通知投保人、被保险人补充提供。</p>
    <p>第十二条 保险人收到被保险人的给付保险金的请求后，应当及时作出是否属于保险责任的核定；情形复杂的，应当在三十日内做出核定，但本保险合同另有约定的除外。</p>
    <p>险人应当将核定结果通知被保险人；对属于保险责任的，在与被保险人达成给付保险金的协议后十日内，履行给付保险金义务。保险合同对给付保险金的期限有约定的，保险人应当按照约定履行给付保险金的义务。保险人依照前款约定作出核定后，对不属于保险责任的，应当自作出核定之日起三日内向被保险人发出拒绝给付保险金通知书，并说明理由。</p>
    <p>第十三条 保险人自收到给付保险金的请求和有关证明、资料之日起六十日内，对其给付的数额不能确定的，应当根据已有证明和资料可以确定的数额先予支付；保险人最终确定给付的数额后，应当支付相应的差额。</p>
    <h3>投保人、被保险人义务</h3>
    <p>第十四条 除另有约定外，投保人应当在保险合同成立时交清保险费。</p>
    <p>第十五条 订立保险合同，保险人就被保险人的有关情况提出询问的，投保人应当如实告知。</p>
    <p>投保人故意或者因重大过失未履行前款规定的义务，足以影响保险人决定是否同意承保或者提高保险费率的，保险人有权解除本保险合同。</p>
    <p>前款规定的合同解除权，自保险人知道有解除事由之日起，超过三十日不行使而消灭。自合同成立之日起超过二年的，保险人不得解除合同；发生保险事故的，保险人应当承担给付保险金责任。</p>
    <p>投保人故意不履行如实告知义务的，保险人对于合同解除前发生的保险事故，不承担给付保险金责任，并不退还保险费。</p>
    <p>投保人因重大过失未履行如实告知义务，对保险事故的发生有严重影响的，保险人对于合同解除前发生的保险事故，不承担给付保险金责任，但应当退还保险费。</p>
    <p>保险人在合同订立时已经知道投保人未如实告知的情况的，保险人不得解除合同；发生保险事故的，保险人应当承担给付保险金责任。</p>
    <p>第十六条 投保人住所或通讯地址变更时，应及时以书面形式通知保险人。投保人未通知的，保险人按本保险合同所载的最后住所或通讯地址发送的有关通知，均视为已发送给投保人。</p>
    <p>第十七条 投保人、被保险人或者保险金受益人知道保险事故发生后，应当在48小时内及时通知保险人。故意或者因重大过失未及时通知，致使保险事故的性质、原因、损失程度等难以确定的，保险人对无法确定的部分，不承担给付保险金责任，但保险人通过其他途径已经及时知道或者应当及时知道保险事故发生的除外。</p>
    <p>上述约定，不包括因不可抗力而导致的迟延。</p>
    <h3>保险金申请与给付</h3>
    <p>第十八条 保险金申请人向保险人申请给付保险金时，应提交以下材料。保险金申请人因特殊原因不能提供以下材料的，应提供其他合法有效的材料。保险金申请人未能提供有关材料，导致保险人无法核实该申请的真实性的，保险人对无法核实部分不承担给付保险金的责任。若保险金申请人委托他人申请的，还应提供授权委托书原件、委托人和受托人的身份证明等相关证明文件。受益人为无民事行为能力人或者限制民事行为能力人的，由其监护人代为申领保险金，并需要提供监护人的身份证明等资料。</p>
    <p>（一）身故保险金申请</p>
    <p>1、保险金给付申请书；</p>
    <p>2、保险合同原件或其他保险凭据；</p>
    <p>3、借款合同；</p>
    <p>4、被保险人的还贷记录或证明；</p>
    <p>5、保险金申请人的身份证明或营业执照、组织机构代码证等证明资料； </p>
    <p>6、公安部门、司法部门或二级及二级以上医院或保险人认可的医疗机构出具的被保险人身故证明书。若被保险人为宣告死亡，保险金申请人应提供法院出具的宣告死亡证明文件；</p>
    <p>7、被保险人的户籍注销证明；</p>
    <p>8、保险金申请人所能提供的与确认保险事故的性质、原因、损失程度等有关的其他证明和资料；</p>
    <p>除提交上述材料外，保险金申请人申请身故保险金时，为确定事故原因，保险人有权要求由司法鉴定机构对事故原因进行鉴定，如进行尸体检验等。</p>
    <p>（二）残疾保险金申请</p>
    <p>1、保险金给付申请书；</p>
    <p>2、保险合同原件或其他保险凭据；</p>
    <p>3、借款合同；</p>
    <p>4、被保险人的还贷记录或证明；</p>
    <p>5、被保险人身份证明或营业执照、组织机构代码证等证明资料； </p>
    <p>6、二级及二级以上医院或保险人认可的医疗机构或司法鉴定机构出具的伤残鉴定诊断书；</p>
    <p>7、保险金申请人所能提供的与确认保险事故的性质、原因、损失程度等有关的其他证明和资料。</p>
    <p>第十九条 保险金申请人向保险人请求给付保险金的诉讼时效期间为二年，自其知道或者应当知道保险事故发生之日起计算。</p>
    <h3>争议处理和法律适用</h3>
    <p>第二十条 因履行本保险合同发生的争议，由当事人协商解决。协商不成的，提交保险单载明的仲裁机构仲裁；保险单未载明仲裁机构且争议发生后未达成仲裁协议的，依法向保险人所在地人民法院起诉。</p>
    <p>第二十一条 与本保险合同有关的以及履行本保险合同产生的一切争议处理适用中华人民共和国法律（不包括香港、澳门及台湾地区法律）。</p>

    <h3>其他事项</h3>
    <p>第二十二条 投保人和保险人可以协商变更保险合同内容。</p>
    <p>变更保险合同的，应当由保险人在保险单或者其他保险凭证上批注或附贴批单，或者投保人和保险人订立变更的书面协议。</p>
    <p>第二十三条 在本保险合同成立后，投保人可以书面形式通知保险人解除合同。</p>
    <p>投保人解除本保险合同时，应提供下列证明文件和资料：</p>
    <p>（一）保险合同解除申请书；</p>
    <p>（二）保险合同原件或其他保险凭据；</p>
    <p>（三）保险费交付凭证；</p>
    <p>（四）投保人身份证明。</p>
    <p>投保人要求解除本保险合同，自保险人接到保险合同解除申请书之时起，本保险合同的效力终止。</p>
    <p>第二十四条 在保险单中载明的保险责任起始日前，投保人要求解除本合同的，保险人退还已收取的保险费。</p>
    <p>在保险单中载明的保险责任起始日后解除本保险合同的，除另有约定外，保险人应向投保人退还未满期净保险费。</p>
    <h3>释义</h3>
    <p>第二十五条 本保险合同涉及下列术语时，适用下列释义：</p>
    <p>（一）周岁：以法定身份证明文件中记载的出生日期为基础计算的实足年龄。</p>
    <p>（二）保险人：指与投保人签订本保险合同的众安在线财产保险股份有限公司。</p>
    <p>（三）意外伤害：指以外来的、突发的、非本意的和非疾病的客观事件为直接且单独的原因致使身体受到的伤害。自然死亡、疾病身故、猝死、自杀以及自伤均不属于意外伤害。</p>
    <p>（四）猝死：外表看似健康的人由于潜在的疾病或者功能障碍所引起的突然的意外的死亡。</p>
    <p>（五）高风险运动：</p>
    <p>指潜水、跳伞、热气球运动、滑翔机、滑翔翼、滑翔伞、动力伞、攀岩运动、探险活动、武术比赛、摔跤比赛、特技表演、赛马、赛车及保险合同载明的其他运动。</p>
    <p>潜水：指以辅助呼吸器材在江、河、湖、海、水库、运河等水域进行的水下运动，但穿着救生衣在水面进行的浮潜活动除外。</p>
    <p>热气球运动：指乘热气球升空飞行的体育活动。</p>
    <p>攀岩活动：指攀登悬崖、楼宇外墙、人造悬崖、冰崖、冰山等运动。</p>
    <p>探险活动：指明知在某种特定的自然条件下有失去生命或使身体受到伤害的危险，而故意使自己置身其中的行为。如江河漂流、非固定路线徒步、徒步穿越沙漠或人迹罕至的原始森林等活动。</p>
    <p>武术比赛：指两人或两人以上对抗性柔道、空手道、跆拳道、散打、拳击等各种拳术及各种使用器械的对抗性比赛。</p>
    <p>特技：指从事马术、杂技、驯兽等特殊技能。</p>
    <p>（六）酒后驾驶：指经检测或者鉴定，发生事故时车辆驾驶人员每百毫升血液中的酒精含量达到或者超过一定的标准，公安机关交通管理部门依据《道路交通安全法》的规定认定为饮酒后驾驶或者醉酒后驾驶。</p>
    <p>（七）无有效驾驶证驾驶：被保险人存在下列情形之一者：</p>
    <p>（1）无驾驶证或驾驶证有效期已届满；</p>
    <p>（2）驾驶的机动车与驾驶证载明的准驾车型不符；</p>
    <p>（3）实习期内驾驶公共汽车、营运客车或者载有爆炸物品、易燃易爆化学物品、剧毒或者放射性等危险物品的机动车，实习期内驾驶的机动车牵引挂车；</p>
    <p>（4）持未按规定审验的驾驶证，以及在暂扣、扣留、吊销、注销驾驶证期间驾驶机动车；</p>
    <p>（5）使用各种专用机械车、特种车的人员无国家有关部门核发的有效操作证，驾驶营业性客车的驾驶人无国家有关部门核发的有效资格证书；</p>
    <p>（6）依照法律法规或公安机关交通管理部门有关规定不允许驾驶机动车的其他情况下驾车。</p>
    <p>（八）无有效行驶证：指下列情形之一：</p>
    <p>（1）机动车被依法注销登记的；</p>
    <p>（2）无公安机关交通管理部门核发的行驶证、号牌，或临时号牌或临时移动证的机动交通工具；</p>
    <p>（3）未在规定检验期限内进行机动车安全技术检验或检验未通过的机动交通工具。</p>
    <p>（九）患艾滋病（AIDS）或感染艾滋病病毒（HIV）：</p>
    <p>艾滋病病毒指人类免疫缺陷病毒，英文缩写为 HIV。艾滋病指人类免疫缺陷病毒引起的获得性免疫缺陷综合征，英文缩写为 AIDS。</p>
    <p>在人体血液或其它样本中检测到艾滋病病毒或其抗体呈阳性，没有出现临床症状或体征的，为感染艾滋病病毒；如果同时出现了明显临床症状或体征的，为患艾滋病。</p>
    <p>（十）不可抗力：指不能预见、不能避免并不能克服的客观情况。</p>
    <p>（十一）保险金申请人：指受益人或被保险人的继承人或依法享有保险金请求权的其他自然人。</p>
    <p>（十二）未满期净保险费：除另有约定外，未满期净保险费=保险费×[1-(保险单已经过天数/保险期间天数)]×（1 -退保手续费率）。经过天数不足一天的按一天计算。退保手续费率由投保人与保险人在投保时约定，并在保险单上载明。</p>



