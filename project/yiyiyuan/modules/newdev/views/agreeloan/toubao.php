    <h3>前言</h3>
    <p>根据保险行业业务发展要求，制订本标准。</p>
    <p>本标准制定过程中参照世界卫生组织《国际功能、残疾和健康分类》（以下简称“ICF”）的理论与方法，建立新的残疾标准的理论架构、术语体系和分类方法。</p>
    <p>本标准制定过程中参考了国内重要的伤残评定标准，如《劳动能力鉴定，职工工伤与职业病致残等级》、《道路交通事故受伤人员伤残评定》等，符合国内相关的残疾政策，同时参考了国际上其他国家地区的伤残分级原则和标准。</p>
    <p>本标准建立了保险行业人身保险伤残评定和保险金给付比例的基础，各保险公司应根据自身的业务特点，根据本标准的方法、内容和结构，开发保险产品，提供保险服务。</p>
    <p>本标准负责起草单位：中国保险行业协会。</p>
    <p>本标准规定了人身保险伤残程度的评定等级以及保险金给付比例的原则和方法，人身保险伤残程度分为一至十级，保险金给付比例分为100%至10%。</p>
    <p>1 适用范围</p>
    <p>本标准适用于意外险产品或包括意外责任的保险产品中的伤残保障，用于评定由于意外伤害因素引起的伤残程度。</p>
    <p>2 术语和定义</p>
    <p>下列术语和定义适用于本标准。</p>
    <p>2.1 伤残：因意外伤害损伤所致的人体残疾。</p>
    <p>2.2 身体结构：指身体的解剖部位，如器官、肢体及其组成部分。</p>
    <p>2.3 身体功能：指身体各系统的生理功能。</p>
    <p>3 标准的内容和结构</p>
    <p>本标准参照ICF有关功能和残疾的分类理论与方法，建立“神经系统的结构和精神功能”、“眼，耳和有关的结构和功能”、“发声和言语的结构和功能”、“心血管，免疫和呼吸系统的结构和功能”、“消化、代谢和内分泌系统有关的结构和功能”、“泌尿和生殖系统有关的结构和功能”、“神经肌肉骨骼和运动有关的结构和功能”和“皮肤和有关的结构和功能” 8大类，共281项人身保险伤残条目。</p>
    <p>本标准对功能和残疾进行了分类和分级，将人身保险伤残程度划分为一至十级，最重为第一级，最轻为第十级。</p>
    <p>与人身保险伤残程度等级相对应的保险金给付比例分为十档，伤残程度第一级对应的保险金给付比例为100%，伤残程度第十级对应的保险金给付比例为10%，每级相差10%。</p>
    <p>4 伤残的评定原则</p>
    <p>4.1 确定伤残类别：评定伤残时，应根据人体的身体结构与功能损伤情况确定所涉及的伤残类别。</p>
    <p>4.2 确定伤残等级：应根据伤残情况，在同类别伤残下，确定伤残等级。</p>
    <p>4.3 确定保险金给付比例：应根据伤残等级对应的百分比，确定保险金给付比例。</p>
    <p>4.4 多处伤残的评定原则：当同一保险事故造成两处或两处以上伤残时，应首先对各处伤残程度分别进行评定，如果几处伤残等级不同，以最重的伤残等级作为最终的评定结论；如果两处或两处以上伤残等级相同，伤残等级在原评定基础上最多晋升一级，最高晋升至第一级。同一部位和性质的伤残，不应采用本标准条文两条以上或者同一条文两次以上进行评定。</p>
    <p>5 说明</p>
    <p>本标准中“以上”均包括本数值或本部位。</p>
